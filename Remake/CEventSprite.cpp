CEventSprite::~CEventSprite()
{

}


//////////////////////////////////////////////////////////////////////////////////////////
// void SetDoDraw(bool doing)
//////////////////////////////////////////////////////////////////////////////////////////
//
// 설명 : 객체가 드로우될지의 여부를 정하는 함수
// 인자 : bool doing(드로우 될지의 여부, Boolean타입)
//
// 2010.01.03 최종수정
//////////////////////////////////////////////////////////////////////////////////////////

void CEventSprite::SetDoDraw(bool doing)
{
	doDraw = doing;
}


//////////////////////////////////////////////////////////////////////////////////////////
// void TextRender(char* text, int x1, int y1, int x2, int y2, int alpha)
//////////////////////////////////////////////////////////////////////////////////////////
//
// 설명 : 텍스트를 출력하는 랩핑함수.
//		  DrawText()를 편하게 사용하고 묶은 함수이다.
// 인자 : 순서대로 출력할 텍스트, 왼쪽위모서리의 x좌표, 왼쪽위모서리의 y좌표, 
//		  오른쪽아래의 x좌표, 오른쪽아래의 y좌표, 알파값(0~255)
//
// 2010.01.20 최종수정
//////////////////////////////////////////////////////////////////////////////////////////

void CEventSprite::TextRender(char* text, int x1, int y1, int x2, int y2, int alpha)
{
	RECT rect = {x1, y1, x2, y2};

	sprite->Begin(D3DXSPRITE_ALPHABLEND);
	pFont->DrawText(sprite, text, -1, &rect, 0, D3DCOLOR_RGBA(255, 255, 255, alpha));
	sprite->End();
}