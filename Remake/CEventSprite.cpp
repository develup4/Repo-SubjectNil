//////////////////////////////////////////////////////////////////////////////////////////

CEventSprite::CEventSprite(LPDIRECT3DDEVICE9 pd3dDevice, LPD3DXSPRITE sprite, LPD3DXFONT pFont)
{
	this->pd3dDevice = pd3dDevice;
	this->sprite = sprite;
	this->pFont = pFont;
}


//////////////////////////////////////////////////////////////////////////////////////////
// ~CEventSprite()
//////////////////////////////////////////////////////////////////////////////////////////
